package org.sagarius.radix.client.collect;

public enum Operator {
	EQ("is"), STARTS_WITH("starts"), ENDS_WITH("ends"), GT("gt"), LT("lt"), HAS(
			"has"), HAS_ALL("hasAll"), REGEX("regex"), LIKE("like"), NOT_LIKE(
			"notlike"), SAME_OBJ("isSameObject"), SAME_ARRAY("isSameArray"), LENGTH(
			"length"), IS_STR("isString"), IS_ARRAY("isArray"), IS_OBJ(
			"isObject"), IS_BOOL("isBoolean"), IS_FUNCTION("isFunction"), IS_NULL(
			"isNull"), IS_UNDEF("isUndefined"), IS_NUM("isNumber");
	private String operator;

	private Operator(String operator) {
		this.operator = operator;
	}

	public String getOperator() {
		return operator;
	}
}
